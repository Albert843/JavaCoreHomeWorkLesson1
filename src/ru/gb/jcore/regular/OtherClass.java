package ru.gb.jcore.regular;

/**
 * Другой класс приложения, его назначение,
 * способы взаимодействия с ним.
 */
public class OtherClass {
    /**
     * Метод сложения двух целых чисел
     * @param a первое слагаемое
     * @param b второе слагаемое
     * @return сумма.
     */
    public  static int add(int a, int b) {
        return a + b;
    }

    /**
     * Метод вычитания двух чисел
     * @param a уменьшаемое
     * @param b вычитаемое
     * @return разность
     */
    public  static int sub(int a, int b) {
        return a - b;
    }

    /**
     * Метод умножения двух чисел
     * @param a первый множитель
     * @param b второй множитель
     * @return произведение
     */
    public  static int mul(int a, int b) {
        return a * b;
    }

    /**
     * Метод деления двух чисел
     * @param a делимое
     * @param b делитель
     * @return частное
     */
    public  static int div(int a, int b) {
        return a / b;
    }

}
